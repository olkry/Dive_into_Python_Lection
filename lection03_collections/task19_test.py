my_list = [2, 4, 6, 2, 8, 10, 12, 14, 16, 18]
print(my_list[2:6:2])  # 6 8
print("----------------")
print(my_list.pop())  # 2, 4, 6, 2, 8, 10, 12, 14, 16 !!!!! 18
print("----------------")
print(my_list.extend([314, 42]))  # 2, 4, 6, 2, 8, 10, 12, 14, 16, 18, 314  !!! None
print("----------------")
print(my_list.sort(reverse=False))  # 2, 2, 4, 6, 8, 10, 12, 14, 16, 18  !!! None
print("----------------")
print(my_list)  # 2, 4, 6, 2, 8, 10, 12, 14, 16, 18 !!!! [2, 2, 4, 6, 8, 10, 12, 14, 16, 42, 314]
